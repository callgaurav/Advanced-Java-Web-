package store;

/**
 *
 * @author Dell
 */
public class New
{
    private String pid;
    private int count;
    
    public New()
    {
        pid = "";
        count = 1;
    }

    /**
     * @return the pid
     */
    public String getPid() {
        return pid;
    }

    /**
     * @param pid the pid to set
     */
    public void setPid(String pid) {
        this.pid = pid;
    }
     
    public void increase()
    {
       this.count = this.count + 1;
    }

    /**
     * @return the count
     */
    public int getCount() {
        return count;
    }

    /**
     * @param count the count to set
     */
    public void setCount(int count) {
        this.count = count;
    }
}
